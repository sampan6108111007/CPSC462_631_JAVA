public class b1 {

  public static void main(String args[]) {
    int s1 = Integer.parseInt(args[0]);
    int s2 = Integer.parseInt(args[1]);
    int s3 = Integer.parseInt(args[2]);
    int s4 = Integer.parseInt(args[3]);
    System.out.println("Plus = " + (s1 + s2 + s3 + s4));
    System.out.println("Minus = " + (s1 - s2 - s3 - s4));
    System.out.println("Multiplied = " + (s1 * s2 * s3 * s4));
    System.out.println("Divide = " + (s1 / s2 / s3 / s4));
  }
}
